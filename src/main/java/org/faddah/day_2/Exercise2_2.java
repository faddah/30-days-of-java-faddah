void main() {
    int a = 10;
    int b = 3;

    // Basic arithmetic operations
    int sum = a + b;
    int difference = a - b;
    int product = a * b;
    int quotient = a / b;
    int remainder = a % b;

    // Display results
    System.out.println("\n\t\t\tDay 2 Exercise 2 - Arithmatic Operations\n");
    System.out.println("\t\ta = " + a);
    System.out.println("\t\tb = " + b);
    System.out.println("\t\tSum: " + sum);
    System.out.println("\t\tDifference: " + difference);
    System.out.println("\t\tProduct: " + product);
    System.out.println("\t\tQuotient: " + quotient);
    System.out.println("\t\tRemainder: " + remainder + "\n");
}
