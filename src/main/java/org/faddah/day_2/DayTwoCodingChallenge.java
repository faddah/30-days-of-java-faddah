void main() {
  // Declare variables for length and width
  double l = 10.0;
  double w = 5.0;
  // Calculate area and perimeter
  double a = l * w;
  double p = 2 * (l + w);
  // Display the results
  System.out.println("\n\t\t\tRectangle Calculator\n");
  System.out.println("\t\tLength: " + l);
  System.out.println("\t\tWidth: " + w);
  System.out.println("\t\tArea: " + a);
  System.out.println("\t\tPerimeter: " + p);
}